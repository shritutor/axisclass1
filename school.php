       $(document).ready(function() {
    // Handle search button click
    $('#searchSchoolButton').click(function() {
        var schoolName = $('#searchSchoolInput').val().trim();
        if (schoolName !== '') {
            searchSchool(schoolName);
        }
    });

    
  // Perform search on input change
    $('#searchSchoolInput').on('input', function() {
        var schoolName = $(this).val().trim();
        if (schoolName !== '') {
            searchSchool(schoolName);
        }
    });

    // Function to perform school search
    function searchSchool(schoolName) {
        $.ajax({
            url: './backend/search_school.php', // Replace with the actual URL to your server-side script
            type: 'GET',
            data: { schoolName: schoolName },
            dataType: 'json',
            success: function(response) {
                if (response.success) {
                    // Display search results
                    var results = response.results;
                    var html = '';

                    if (results.length > 0) {
                        for (var i = 0; i < results.length; i++) {
                            var school = results[i];
                            html += '<div class="bg-info mt-5 p-5">';
                            html += '<div class="d-flex flex-column justify-content-end w-100 mt-3">';
                            html += '<div class="d-flex">';
                            html += '<div class="d-flex flex-column">';
                            html += '<p><span class="font-weight-bold">Name:- </span>' + school.schoolname + '</p>';
                            html += '<p class="data_card"><span class="font-weight-bold ">U dise number:- </span>' + school.udisenumber + '</p>';
                            html += '<p class="data_card"><span class="font-weight-bold">Registration number:- </span>' + school.registrationnumber + '</p>';
                            html += '<p class="data_card"><span class="font-weight-bold">Area name:- </span>' + school.areaname + '</p>';
                            html += '<p class="data_card"><span class="font-weight-bold">Pin code:- </span>' + school.pincode + '</p>';
                            html += '<p class="data_card"><span class="font-weight-bold">City:- </span>' + school.city + '</p>';
                            html += '<p class="data_card"><span class="font-weight-bold">Phone number.:- </span>' + school.phonenumber + '</p>';
                            html += '<p class="data_card"><span class="font-weight-bold">Email address:- </span>' + school.email + '</p>';
                            html += '</div>';
                            html += '<img class="school_pic_logo" src="' + school.imageurl + '">';
                            html += '</div>';
                            html += '<a class="btn btn-primary mt-5" href="redirectschool.php?id=' + school.id + '">Join School</a>';

                            html += '</div>';
                            html += '</div>';
                        }
                    } else {
                        html = '<p>No schools found.</p>';
                    }

                    $('#schoolResults').html(html);
                } else {
                    // Display error message
                    $('#schoolResults').html('<p>Error: ' + response.message + '</p>');
                }
            },
            error: function() {
                // Display error message
                $('#schoolResults').html('<p>Error performing search.</p>');
            }
        });
    }
});
